//operators
public class class8 {

    public static void main(String[] args) {

        //Arithmatic Operator
        int a = 9;
        int b = 10 + a;
        System.out.println(b);

        //Assignment Operator
        int c = 10;
        c += 10;
        System.out.println(c);

        //Comparison Operator
        System.out.println(6==6);
        System.out.println(6>=10);
        System.out.println(6 <= 10);

        //Logical Operator
        System.out.println(6<5 && 6<10);
        System.out.println(6<10 || 6>10 );
        
        
    }
}
